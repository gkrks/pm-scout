import { z } from "zod";
import * as fs from "fs";
import * as path from "path";
import { loadRoutingConfig, resolveRouting } from "./loadRouting";

// ── Selector config (custom-playwright only) ──────────────────────────────────

export const SelectorsSchema = z.object({
  jobCard:         z.string(),
  title:           z.string(),
  location:        z.string().optional(),
  applyUrl:        z.string(),
  postedDate:      z.string().optional(),
  postedDateAttr:  z.string().optional(),   // attribute to read date from (e.g. "datetime")
  scrollToLoad:    z.boolean().optional(),
  waitForSelector: z.string().optional(),
  timeoutMs:       z.number().optional(),
});

// ── Company config (internal representation) ──────────────────────────────────

export const ATS_VALUES = [
  "greenhouse",
  "lever",
  "ashby",
  "workday",
  "smartrecruiters",
  "workable",
  "bamboohr",
  "amazon",
  "google-playwright",
  "meta-playwright",
  "custom-playwright",
  "manual",
] as const;

export type AtsValue = typeof ATS_VALUES[number];

export const CompanyConfigSchema = z.object({
  /** Deterministic UUID from the external targets JSON (uuidv5 from slug). */
  id:             z.string().optional(),
  name:           z.string().min(1),
  ats:            z.enum(ATS_VALUES),
  slug:           z.string().nullable(),
  careersUrl:     z.string().url(),
  roles:          z.array(z.string()).default([]),
  enabled:        z.boolean().default(true),
  earlyCareerUrl: z.string().url().optional(),
  selectors:      SelectorsSchema.optional(),
}).refine(
  (c) => c.ats !== "custom-playwright" || c.selectors !== undefined,
  { message: "selectors is required when ats is 'custom-playwright'" },
);

// ── Defaults ──────────────────────────────────────────────────────────────────

export const DefaultsSchema = z.object({
  maxExperienceYears: z.number().default(3),
  locationFilter:     z.string().default("US"),
  dateCutoffDays:     z.number().default(30),
});

// ── Internal targets format ───────────────────────────────────────────────────

export const TargetsConfigSchema = z.object({
  version:   z.literal(1),
  defaults:  DefaultsSchema,
  companies: z.array(CompanyConfigSchema),
});

export type SelectorsConfig = z.infer<typeof SelectorsSchema>;
export type CompanyConfig   = z.infer<typeof CompanyConfigSchema>;
export type TargetsConfig   = z.infer<typeof TargetsConfigSchema>;

// ── External targets format (config/targets.json generated by user) ───────────
// This schema is flexible — we only validate the parts we actually read.

const ExternalCompanySchema = z.object({
  uuid:               z.string().optional(),
  slug:               z.string(),
  name:               z.string(),
  category:           z.string().optional(),
  careers_url:        z.string(),
  program_url:        z.string().nullable().optional(),
  has_apm_program:    z.boolean().optional(),
  apm_program_name:   z.string().nullable().optional(),
  apm_program_status: z.string().nullable().optional(),
  domain_tags:        z.array(z.string()).optional(),
  target_roles:       z.array(z.string()).optional(),
  notes:              z.string().nullable().optional(),
  index:              z.number().optional(),
  content_hash:       z.string().optional(),
  // Optional ATS override — if the user adds this field to a company entry
  ats:                z.string().optional(),
});

const ExternalTargetsConfigSchema = z.object({
  metadata: z.object({
    title:            z.string().optional(),
    version:          z.string().optional(),
    schema_version:   z.string().optional(),
    total_companies:  z.number().optional(),
  }).passthrough().optional(),
  filters: z.object({
    target_roles:            z.array(z.string()).optional(),
    title_include_keywords:  z.array(z.string()).optional(),
    title_exclude_keywords:  z.array(z.string()).optional(),
    experience: z.object({
      max_years:          z.number().optional(),
      reject_above_years: z.number().optional(),
    }).passthrough().optional(),
    freshness: z.object({
      max_posting_age_days: z.number().optional(),
    }).passthrough().optional(),
  }).passthrough().optional(),
  ranking_rules:            z.record(z.unknown()).optional(),
  categories:               z.array(z.string()).optional(),
  company_field_descriptions: z.record(z.string()).optional(),
  companies: z.array(ExternalCompanySchema),
}).passthrough();

type ExternalCompany = z.infer<typeof ExternalCompanySchema>;

